package hdt6;

/**
 *
 * @author Gustavo
 */
public class Carta {
    private String nombre;
    private String tipo;
    private int num;

    public Carta(String nombre, String tipo, int num) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.num = num;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
    
    
    
}
